    class StringPaintedProgressBarUI extends BasicProgressBarUI {
    @Override
    protected void paintDeterminate(Graphics g, JComponent c) {
        super.paintDeterminate(g, c);
            if (progressBar.isStringPainted()) {
                Insets b = progressBar.getInsets(); // Get the insets (borders)
                int barRectWidth = progressBar.getWidth() - (b.right + b.left);
                int barRectHeight = progressBar.getHeight() - (b.top + b.bottom);
                int fillRectWidth = (int) ((barRectWidth - 2) * progressBar.getPercentComplete()); // Subtract 2 for spacing

                if (progressBar.getOrientation() == JProgressBar.HORIZONTAL) {
                    int x = b.left + 1; // Add 1 for spacing
                    int y = b.top;
                    int w = fillRectWidth;
                    int h = barRectHeight;
                    g.setColor(progressBar.getForeground());
                    paintString(g, x, y, w, h, 0, b);
                }
            }
        }
    } 
    
