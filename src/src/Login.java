    private void loginbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbuttonActionPerformed
        // TODO add your handling code here:
        String userIn = usernamelogin.getText();
        String passIn = passwordlogin.getText();

        if (ValidateCredentials(userIn, passIn)) {
            loading.setUI(new StringPaintedProgressBarUI());
            loading.setVisible(true);
            loading.setString("Logging In");

            Timer timer = new Timer(5, new ActionListener() {
            private int progress = 0;

            @Override
            public void actionPerformed(ActionEvent e) {
                progress++;
                loading.setValue(progress);

                if (progress >= 100) {
                    ((Timer) e.getSource()).stop();
                    Summary c = new Summary(userIn);
                    c.setVisible(true);
                    setVisible(false);
                }
            }
        });
        timer.start();
        } else if (!userIn.equals(Register.uname) && passIn.equals(Register.pword)) {
            usernamelogin.setBorder(BorderFactory.createLineBorder(Color.red));
        } else if (userIn.equals(Register.uname) && !passIn.equals(Register.pword)) {
            passwordlogin.setBorder(BorderFactory.createLineBorder(Color.red));
        } else {
            usernamelogin.setBorder(BorderFactory.createLineBorder(Color.red));
            passwordlogin.setBorder(BorderFactory.createLineBorder(Color.red));
        }
    }//GEN-LAST:event_loginbuttonActionPerformed
    
    private boolean ValidateCredentials(String username, String password) {
        try (BufferedReader reader = new BufferedReader(new FileReader(AccStorage.getfilepath()))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] data = line.split(",");
                    String savedusername = data[0].trim();
                    String savedpassword = data[1].trim();
                    if (username.equals(savedusername) && password.equals(savedpassword)) {
                        return true;
                    }
            }
        } catch (IOException e) {
            e.printStackTrace();
        } return false;
    }
